export function translate(locale: Locale, key: string, vars?: Record<string, string | number>) {
  const value = messages[locale][key] ?? messages.en[key] ?? key;
  return typeof value === "function" ? value({ vars }) : value;
}

export function getLocaleTag(locale: Locale) {
  return locale === "uz" ? "uz-UZ" : locale === "ru" ? "ru-RU" : "en-US";
}

export function translateCategory(locale: Locale, value: string) {
  return translate(locale, `skin.category.${value}`);
}

export function translateRarity(locale: Locale, value: string) {
  return translate(locale, `skin.rarity.${value}`);
}

export function translateExterior(locale: Locale, value: string) {
  return translate(locale, `skin.exterior.${value}`);
}

export function translateKpiLabel(locale: Locale, value: string) {
  const key = kpiLabelMap[value];
  return key ? translate(locale, key) : value;
}

export function translateKpiHelper(locale: Locale, value: string) {
  const direct = kpiHelperMap[value];
  if (direct) return translate(locale, direct);
  const watchlistMatch = value.match(/^(\d+) items on watchlist$/);
  if (watchlistMatch) return `${watchlistMatch[1]} ${locale === "ru" ? "предм. в watchlist" : locale === "uz" ? "ta item watchlistda" : "items on watchlist"}`;
  const notifMatch = value.match(/^(\d+) fresh notifications$/);
  if (notifMatch) return `${notifMatch[1]} ${locale === "ru" ? "новых уведомлений" : locale === "uz" ? "ta yangi bildirishnoma" : "fresh notifications"}`;
  return value;
}

