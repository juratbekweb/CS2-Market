export function localizeSkin<T extends Pick<SkinCard, "slug" | "name" | "category" | "rarity" | "exterior" | "finishStyle" | "description">>(locale: Locale, skin: T) {
  return {
    ...skin,
    name: skin.name,
    category: translateCategory(locale, skin.category),
    rarity: translateRarity(locale, skin.rarity),
    exterior: translateExterior(locale, skin.exterior),
    finishStyle: translate(locale, `skin.finish.${skin.slug}`),
    description: translate(locale, `skin.desc.${skin.slug}`),
  };
}
