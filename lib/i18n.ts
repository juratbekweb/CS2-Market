export function translateTransactionDescription(locale: Locale, value: string) {
  const exactMap: Record<string, string> = {
    "Card deposit": "txn.cardDeposit",
    "Mock deposit": "txn.mockDeposit",
    "Mock withdrawal": "txn.mockWithdrawal",
    "Marketplace commission": "txn.marketCommission",
    "Wallet deposit": "txn.walletDeposit",
    "Wallet withdrawal": "txn.walletWithdrawal",
  };
  if (exactMap[value]) return translate(locale, exactMap[value]);
  if (value.startsWith("Bought ")) return translate(locale, "action.bought", { item: value.slice(7) });
  if (value.startsWith("Sold ")) return translate(locale, "action.sold", { item: value.slice(5) });
  if (value.startsWith("Commission charged on ")) return translate(locale, "action.commissionCharged", { item: value.slice(22) });
  return value;
}

